//====================================================================
//GetBookMove() returns a book move for the current position.
//Calls SelectBookMove() which chooses the move.
//====================================================================
const char *CBook::GetBookMove(const CBookPos *cur_pos) {
  const char *promote = "  NBRQ";
  static char buffer[6];
  int i, move;
  char *pc = buffer;
  if ((move = SelectBookMove(cur_pos))) {
    //book move available - format to ascii
    for (i = 0; i < 2; i++) {
      *pc++ = (char) ('a' + (move & 7));
      *pc++ = (char) ('1' + ((move & 63) >> 3));
      move >>= 6;
    }
    i = move & 7;
    if ((i > 1) && (i < 6)) *pc++ = promote[i];
  }
  *pc = 0;
  return buffer;
} //GetBookMove()

//====================================================================
//SelectBookMove() makes the random selection of a move according to
//move weight.  Calls FindBookEntry() to get moves to select from.
//====================================================================
int CBook::SelectBookMove(const CBookPos *cur_pos) {
  int i, k = 0;
  char c;
  //do a quick validity check and piece count
  for (i = 0; i < 64; i++) {  
    c = cur_pos->bk_board[i];
    if (c) {
      k++;
      if ((c < 0) || (c > 15)) return 0;
    }
  }
  if (k < bk_min_men) return 0;             //too few pieces
  if (!FindBookEntry(cur_pos)) return 0;    //position not found
  if (bk_num_moves == 0) return 0;          //position has no moves

  k = 0;
  c = (char) (rand() % 100);                //0 to 99
  for (i = 0; i < bk_num_moves; i++) {
    k += bk_entry.bk_value[i];
    if (k > c) return (bk_entry.bk_moves[i]);
  }  
  return (0);   //no move
} //SelectBookMove()

//====================================================================
//FindBookEntry() looks for the current position in the book.  Returns 
//true if the position is found and member variable bk_entry will have 
//the moves.  Looks first for the current position and, if not found,
//looks again for the position with colors reversed.
//Calls BinSearch() to perform the actual search.
//====================================================================
bool CBook::FindBookEntry(const CBookPos *cur_pos) {
  int i, move, b1, b2, pro;
  char c;
  CBookPos temp;

  bk_num_moves = 0;
  bk_color_rev = 0;
  if ((i = BinSearch(GetHashKey(cur_pos)))) {
    CopyBookEntry(i);
    return 1;
  }
  //failed to find position, reverse colors and try again
  for (i = 0; i < 64; i++) {
    c = cur_pos->bk_board[i];
    if (c) c ^= 8;                    //toggle piece color
    temp.bk_board[Flip(i)] = c;      //flip square
  }
  c = cur_pos->bk_ep_target;
  if ((c < 16) || (c > 47)) c = 0;
  if (c) c = (char) Flip(c);
  temp.bk_ep_target = c;
  temp.bk_color = !cur_pos->bk_color;
  i = BinSearch(GetHashKey(&temp));
  if (!i) return 0;
  //found the color reverse position. moves have to be reversed
  bk_color_rev = 1;
  CopyBookEntry(i);
  for (i = 0; i < bk_num_moves; i++) {
    move = bk_entry.bk_moves[i];
    b1 = Flip(move & 63);
    b2 = Flip((move >> 6) & 63);
    pro = (move >> 12) & 7;
    bk_entry.bk_moves[i] = (short)(b1 + (b2 << 6) + (pro << 12));
  }
  return 1;
} //FindBookEntry()

//====================================================================
//BinSearch() performs search for target position.  Since the keys 
//are random, the value of target tells approximately where it
//resides in the list.  BinSearch() finds a window around target then
//performs a binary search of the window.
//====================================================================
int CBook::BinSearch(bk_bbd target) {
  int lo, mid, hi;
  bk_bbd key;

  //set a window around the calculated position of target
  mid = 1 + (bk_num_pos >> 8);  //window ~ 0.5% of total positions
  hi = (int) (target >> 33);    //whittle down to signed 4 byte size
  hi = mid + (int) (bk_num_pos * (((float)hi)/2147483647.0f));  
  if (hi > bk_num_pos) hi = bk_num_pos;
  lo = hi - 2 * mid;
  if (lo < 1) lo = 1;
  //adjust window if we missed the target
  while (BookEntryKey(lo) > target) {   //window too high
    hi = lo;          //move hi down to lo
    mid *= 2;         //expand window size
    lo -= mid;        //move lo down by window size
    if (lo < 1) {     //check limits
      lo = 1;
      break;
    }
  }
  while (BookEntryKey(hi) < target) {   //window too low
    lo = hi;
    mid *= 2;
    hi += mid;
    if (hi > bk_num_pos) {
      hi = bk_num_pos;
      break;
    }
  }
  //now do a binary search on the window
  while (lo <= hi) {
    mid = (lo + hi) / 2;
    key = BookEntryKey(mid);
    if (target < key) 
      hi = mid - 1;
    else if (target > key)
      lo = mid + 1;
    else
      return mid;       //found the position
  }
  return 0;             //search fails
} //BinSearch()

//====================================================================
//Utility Routines:
//Flip() color reverses a square
//GetHashKey() returns hash key for a CBookPos
//BookEntryKey() returns the key corresponding to index passed
//CopyBookEntry() copies entry to bk_entry and sets number of moves
//====================================================================
int CBook::Flip(int sq) {
  return 8*(7-(sq >> 3)) + (sq & 7);
} //Flip()

bk_bbd CBook::GetHashKey(const CBookPos *p1) {
  int i;
  char c;
  bk_bbd key = 0;
  for (i = 0; i < 64; i++) {
    if ((c = p1->bk_board[i])) key ^= bk_rnd[c][i];
  }
  //bk_rnd[8][0] is black-to-move
  if (p1->bk_color) key ^= bk_rnd[8][0];
  //bk_rnd[0][sq] is en passant target
  c = p1->bk_ep_target;
  if ((c < 16) || (c > 47)) c = 0;
  if (c) key ^= bk_rnd[0][c];
  return key;
} //GetHashKey()

bk_bbd CBook::BookEntryKey(int ix) {
  if (bk_using_default) {
    return bk_def_book[ix].bk_key;
  }
  //future - file
  return 0;
} //BookEntryKey()

void CBook::CopyBookEntry(int ix) {
  int i;
  if (bk_using_default) {
    bk_entry.bk_key = bk_def_book[ix].bk_key;
    for (i = 0; i < BK_MAX_MOVES; i++) {
      bk_entry.bk_moves[i] = bk_def_book[ix].bk_moves[i];
      bk_entry.bk_value[i] = bk_def_book[ix].bk_value[i];
    }
  } else {
    //future - file
    bk_entry.bk_moves[0] = 0;   //no moves
  }
  //set the number of moves
  for (i = 0; i < BK_MAX_MOVES; i++) {
    if (!bk_entry.bk_moves[i]) break;
  }
  bk_num_moves = i;
} //CopyBookEntry()

//====================================================================
//InitBook() is called at startup by constructor.  All it does is set
//the default book.  Future plans are too look for a book on disk
//and set a file pointer to that if found.
//====================================================================
void CBook::InitBook() {
//seed random numbers unless told not to
#if seed_random_number_generator
  srand((unsigned)time(NULL));
#endif
  extern bk_bbd random_numbers[];
  //cast random_numbers[1024] to bk_rnd[16][64]
  bk_rnd = (bk_bbd (*)[64]) random_numbers;
  //set default book
  bk_using_default = 1;
  //first record has number positions and minimum pieces
  bk_num_pos = (int) bk_def_book[0].bk_key;
  bk_min_men = bk_def_book[0].bk_moves[0];
  //future - file
} //InitBook()

